//Задайте массив заполненный случайными трёхзначными числами. Напишите программу, которая покажет количество чётных чисел в массиве.

using System;

class Program
{
    static void Main()
    {
        // Создаем массив из случайных трехзначных чисел
        int[] массив = СоздатьСлучайныйМассив(10);

        // Находим количество четных чисел в массиве
        int количествоЧетных = ПодсчитатьЧетныеЧисла(массив);

        // Выводим результат
        Console.WriteLine("Сгенерированный массив:");
        ВывестиМассив(массив);
        Console.WriteLine($"Количество четных чисел в массиве: {количествоЧетных}");
    }

    // Метод для создания массива случайных трехзначных чисел
    static int[] СоздатьСлучайныйМассив(int размер)
    {
        Random rand = new Random();
        int[] массив = new int[размер];

        for (int i = 0; i < размер; i++)
        {
            массив[i] = rand.Next(100, 1000); // Генерируем трехзначные числа
        }

        return массив;
    }

    // Метод для подсчета четных чисел в массиве
    static int ПодсчитатьЧетныеЧисла(int[] массив)
    {
        int количествоЧетных = 0;

        foreach (int число in массив)
        {
            if (число % 2 == 0)
            {
                количествоЧетных++;
            }
        }

        return количествоЧетных;
    }

    // Метод для вывода массива на экран
    static void ВывестиМассив(int[] массив)
    {
        foreach (int число in массив)
        {
            Console.Write(число + " ");
        }
        Console.WriteLine();
    }
}